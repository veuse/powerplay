<h1>Posts</h1>

<h3>Post formats</h3>

<div class="about-text"><?php echo $theme_name;?> supports Wordpress post-formats. Post format are a method of styling posts. By using this feature, you can specify the display-format of a specific post.</div>

<br>
<hr>
<br>

<h4>Image format</h4>
<p>If format is set to "Image" it will display the featured image in the single post.</p>

<h4>Gallery format</h4>
<p>Displays a gallery in the post. Gallery images need to be defined in the "Image Gallery" meta-panel.</p>

<h4>Video format</h4>
<p>Displays a video in the post. Video source needs to be defined "Video" meta-panel.</p>

<h4>Standard format</h4>
<p>Does not do anything particular ( Will not show any featured media ).</p>


